// Stylish and Well-Structured Variable Declarations

// String
let name = "Kashif Shaikh";

// Number
let age = 22;

// Boolean
let isStudent = true;

// Null
let height = 6;

// Undefined
let weight = 53;

// Object
let person = {
    firstName: "Sam ",
    lastName: "Shaikh"
};

// Array
let colors = ["red", "green", "blue"];

// Printing each variable to the console
console.log("Name:", name);
console.log("Age:", age);
console.log("Is Student:", isStudent);
console.log("Height:", height);
console.log("Weight:", weight);
console.log("Person:", person);
console.log("Colors:", colors);

// Stylish Separation
console.log("\n--- Detailed Output ---");

// Detailed Output
console.log(`Name: ${name}`);
console.log(`Age: ${age}`);
console.log(`Is Student: ${isStudent}`);
console.log(`Height: ${height}`);
console.log(`Weight: ${weight}`);
console.log("Person Details:");
console.log(`  First Name: ${person.firstName}`);
console.log(`  Last Name: ${person.lastName}`);
console.log("Favorite Colors:", colors.join(", "));
